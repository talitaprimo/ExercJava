package condicional;

public class OperadorTernario2 {

	public static void main(String[] args) {
		
        int n1 = 14;
        int n2 = 8;
        int r = (n1>n2)?n1+n2:n1-n2;
        System.out.println(r);
    	
        /* 
        * Operador específico para fazer comparações, atribuindo valor a uma
        * mesma variável.
		* O nome operador ternário é dado pois ele tem 3 componentes, o que vem
		* antes da interrogação, o que vem depois da interrogação e o que vem
		* depois dos dois pontos.
		* Condição ? Valor atribuído se essa expressão for verdadeira : Valor
		* atribuído se essa expressão for falsa.
		*/
	}

}
